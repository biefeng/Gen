    private static String byteArrStrToStr(String byteArr) {
        String byteStrs[] = byteArr.trim().replaceAll("[\\s\\t\\n\\r]+", " ").split(" ");
        if (byteStrs.length <= 0) {
            return "";
        }
        byte[] bytes = new byte[byteStrs.length];
        int index = 0;
        for (String s : byteStrs) {
            byte b = (byte) (Integer.parseInt(s, 16) & 0xFF);
            bytes[index++] = b;
        }

        return new String(bytes);
    }

